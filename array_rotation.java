import java.util.*;
public class array_rotation {
    public static void main(String[] args) {
        Scanner obj=new Scanner(System.in);
        int n=obj.nextInt();
        int i;
        int[] arr=new int[n];
        for( i=0;i<n;i++){
            arr[i]=obj.nextInt();
        }
        System.out.println(arr[i]);
    }
}
