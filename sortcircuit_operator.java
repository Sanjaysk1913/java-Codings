/*
 * sort circuit operators
 * &&,||
 */
public class sortcircuit_operator {
    public static void main(String[] args) {
        int a=30,b=25;
        System.out.println((a>10 && a<40)); //AND sort circuit operators
        System.out.println((b>10 && b>40));
        System.out.println((a<10 || a>40)); //OR sort circuit operators
        System.out.println((b<10 || b<40));
    }
}
