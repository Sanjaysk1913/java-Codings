public class arithmetic_operater {
    public static void main(String[] args) {
        int a=10,b=5,c;
        c=a%b;//arithmetic_operater
        System.out.println(c);
        a%=b;//arithmetic_operater 
        System.out.println(a);
        a+=5;//compund assignment
        System.out.println(a); 
        a++;//post increament using only increment 1
        System.out.println(a);
        ++a;//pre increament using only increment 1
        System.out.println(a);
        a=a+5;// using 2 or more values
        System.out.println(a);
        a--;//post decreament using only decrement 1
        System.out.println(a);
        --a;//pre decreament using only decrement 1
        System.out.println(a);
    }
}
